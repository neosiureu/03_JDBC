CREATE TABLE TB_MEMBER(
	MEMBER_NO     NUMBER CONSTRAINT TB_MEMBER_PK PRIMARY KEY,
	MEMBER_ID     VARCHAR2(30) NOT NULL UNIQUE,
	MEMBER_PW     VARCHAR2(30) NOT NULL,
	MEMBER_NAME   VARCHAR2(30) NOT NULL,
	ENROLL_DATE DATE DEFAULT CURRENT_DATE
);

COMMENT ON COLUMN TB_MEMBER.MEMBER_NO     IS '멤버 번호';
COMMENT ON COLUMN TB_MEMBER.MEMBER_ID     IS '멤버 아이디';
COMMENT ON COLUMN TB_MEMBER.MEMBER_PW     IS '멤버 비밀번호';
COMMENT ON COLUMN TB_MEMBER.MEMBER_NAME   IS '멤버 이름';
COMMENT ON COLUMN TB_MEMBER.ENROLL_DATE IS '멤버 가입일';


-- MEMBER_NO 컬럼에 삽입될 시퀀스 생성
