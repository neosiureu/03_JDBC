CREATE TABLE TB_TODO(
	TODO_NO     NUMBER CONSTRAINT TODO_PK PRIMARY KEY,
	TODO_TITLE    VARCHAR2(30) NOT NULL,
	TODO_COMPLETE     CHAR(1) DEFAULT 'N' CHECK (TODO_COMPLETE IN ('Y','N')),
	ENROLL_DATE DATE DEFAULT CURRENT_DATE
);

COMMENT ON COLUMN TB_TODO.TODO_NO      IS '할일 번호';
COMMENT ON COLUMN TB_TODO.TODO_TITLE    IS '할일 제목';
COMMENT ON COLUMN TB_TODO.TODO_COMPLETE    IS '완료여부';
COMMENT ON COLUMN TB_TODO.ENROLL_DATE  IS '등록날짜';


-- TODO_NO 컬럼에 삽입될 시퀀스 생성
